package scenedipity.model;

public  class  RestaurantOrdering{
	private String online_order_type;
	private String online_order_name;
	private String online_order_url;
	public String getOnline_order_type() {
		return online_order_type;
	}
	public void setOnline_order_type(String online_order_type) {
		this.online_order_type = online_order_type;
	}
	public String getOnline_order_name() {
		return online_order_name;
	}
	public void setOnline_order_name(String online_order_name) {
		this.online_order_name = online_order_name;
	}
	public String getOnline_order_url() {
		return online_order_url;
	}
	public void setOnline_order_url(String online_order_url) {
		this.online_order_url = online_order_url;
	}
}
